public void setFields(int id, String usuario, String password, String tipoUsuario, String nombreCompleto,
            String ciPasport, String fNacimiento, String genero, String direccion,
            String telefono, String email) {
        // Llenar los campos con los datos pasados
        jTextField8.setText(usuario);
        jPasswordField1.setText(password);
        jComboBox1.setSelectedItem(tipoUsuario);
        jTextField1.setText(nombreCompleto);
        jTextField2.setText(ciPasport);

        try {
            // Parsear la fecha
            SimpleDateFormat formato = new SimpleDateFormat("yyyy-MM-dd");
            jDateChooser1.setDate(formato.parse(fNacimiento));
        } catch (ParseException e) {
            e.printStackTrace();
        }

        jComboBox2.setSelectedItem(genero);
        jTextField5.setText(direccion);
        jTextField7.setText(telefono);
        jTextField6.setText(email);

        // Establecer el modo de edición para poder actualizar los datos
        modoEdicion = true; // Establecemos que ahora estamos en modo edición
        idUsuarioEditando = id; // Guardamos el ID del usuario que estamos editando
        jButton6.setText("ACTUALIZAR");  // Cambiar el texto del botón
    }

