    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // Instanciamos UsuarioDAO para interactuar con la base de datos
        UsuarioDAO uDAO = new UsuarioDAO();

        try {
            // Obtener los valores de los campos del formulario
            String usuario = jTextField8.getText().trim();
            String password = new String(jPasswordField1.getPassword()).trim();
            String confirmPassword = new String(jPasswordField2.getPassword()).trim();  // Confirmar contraseña
            String tipoUsuario = (String) jComboBox1.getSelectedItem();
            String nombreCompleto = jTextField1.getText().trim();
            String ciPasport = jTextField2.getText().trim();
            String fNacimiento = (jDateChooser1.getDate() != null) ? new SimpleDateFormat("yyyy-MM-dd").format(jDateChooser1.getDate()) : "";
            String genero = (String) jComboBox2.getSelectedItem();
            String direccion = jTextField5.getText().trim();
            String telefono = jTextField7.getText().trim();
            String email = jTextField6.getText().trim();

            // Validaciones básicas para asegurarse de que los campos no estén vacíos
            if (usuario.isEmpty() || password.isEmpty() || nombreCompleto.isEmpty() || ciPasport.isEmpty() || fNacimiento.isEmpty() || genero.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Por favor complete todos los campos requeridos.");
                return;  // Detener la ejecución si faltan campos obligatorios
            }
            // Validación de ComboBox (género y estado civil)
            if (tipoUsuario == null || tipoUsuario.trim().equals("Seleccione una opción")) {
                JOptionPane.showMessageDialog(null, "Por favor seleccione un tipo de usuario.");
                return;  // Detener la ejecución si no se selecciona un estado civil
            }

            if (genero == null || genero.trim().equals("Seleccione una opción")) {
                JOptionPane.showMessageDialog(null, "Por favor seleccione un género.");
                return;  // Detener la ejecución si no se selecciona un género
            }

            // Validar que la contraseña y la confirmación de la contraseña coincidan
            if (!password.equals(confirmPassword)) {
                JOptionPane.showMessageDialog(null, "Las contraseñas no coinciden.");
                return;  // Detener la ejecución si las contraseñas no coinciden
            }
            
            if (!Usuario.validarCedula(ciPasport)) {
                JOptionPane.showMessageDialog(null, "La cédula ingresada no es válida.");
                return;
            }

            // Si estamos en modo edición, actualizamos el usuario
            if (modoEdicion) {
                // Actualizar el usuario existente
                boolean actualizado = uDAO.modificarUsuario(new Usuario(idUsuarioEditando, usuario, password, tipoUsuario, nombreCompleto, ciPasport, fNacimiento, genero, direccion, telefono, email));

                if (actualizado) {
                    JOptionPane.showMessageDialog(null, "Usuario modificado correctamente.");
                    // No cerramos el formulario, solo limpiamos los campos
                    limpiarCampos();  // Limpiar los campos después de la edición
                } else {
                    JOptionPane.showMessageDialog(null, "Error al modificar el usuario.");
                }
            } else {
                // Si no estamos en modo edición, registramos un nuevo usuario
                boolean registrado = uDAO.guardarUsuario(new Usuario(0, usuario, password, tipoUsuario, nombreCompleto, ciPasport, fNacimiento, genero, direccion, telefono, email));

                if (registrado) {
                    JOptionPane.showMessageDialog(null, "Usuario registrado correctamente.");
                    limpiarCampos();  // Limpiar los campos después de registrar
                } else {
                    JOptionPane.showMessageDialog(null, "Error al registrar el usuario.");
                }
            }
        } catch (Exception e) {
            // Capturar cualquier error y mostrarlo en un cuadro de diálogo
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton6ActionPerformed
